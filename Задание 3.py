import math

# Заданные вероятности символов
p1 = 0.3
p2 = 0.4
p = 1 - p1 - p2

# Расчет энтропии источника сообщений
H = -(p1 * math.log2(p1) + p2 * math.log2(p2) + p * math.log2(p))

# Средняя длительность передачи одного элемента сообщения в канале
tau = 1e-6  # 10^-6 с

# Расчет пропускной способности канала
C = 1 / tau

# Расчет скорости передачи информации
R = C * H

print("Пропускная способность канала:", C, "бит/с")
print("Скорость передачи информации:", R, "бит/с")

# Проверка приближения скорости передачи к пропускной способности
if R <= C:
    print("Скорость передачи информации приблизительно равна пропускной способности канала.")
else:
    print("Скорость передачи информации не приблизительно равна пропускной способности канала.")
