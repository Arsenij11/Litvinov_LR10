# Вероятности символов
p1 = 0.3
p2 = 0.4
p3 = 1 - p1 - p2

# Количество бит для каждого символа (равномерный двоичный код)
bits_p1 = 1
bits_p2 = 1
bits_p3 = 2

# Средняя длительность передачи одного элемента сообщения в канале (в секундах)
tau = 10**(-6)

# Пропускная способность канала (в бит/сек)
channel_capacity = (p1 * bits_p1 + p2 * bits_p2 + p3 * bits_p3) / tau

# Скорость передачи информации (в бит/сек)
information_rate = p1 * bits_p1 + p2 * bits_p2 + p3 * bits_p3

print("Пропускная способность канала:", channel_capacity, "бит/сек")
print("Скорость передачи информации:", information_rate, "бит/сек")

# Проверка можно ли приблизить скорость передачи к пропускной способности
if information_rate <= channel_capacity:
    print("Скорость передачи можно приблизить к пропускной способности данной системы.")
else:
    print("Скорость передачи нельзя приблизить к пропускной способности данной системы.")
