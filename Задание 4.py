def find_BCH_parameters(N, t):
    # Находим m
    m = 2 * t
    while True:
        if (2 ** m) - 1 >= N + 2 * t:
            break
        m += 1

    # Находим n
    n = 2 ** m - 1

    # Находим k
    k = n - 2 * t

    return n, k

# Заданные значения
N = 128   # Общее количество разрешенных кодовых комбинаций
t = 2     # Количество исправляемых ошибок

n, k = find_BCH_parameters(N, t)

print("Длина кодового слова (n):", n)
print("Количество информационных символов (k):", k)
print("Количество проверочных символов (r):", 2 * t)
